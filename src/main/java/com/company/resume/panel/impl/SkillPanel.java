    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        User loggedInUser = Config.loggedInUser;
        Skill skill = null;
        if(txtSkill.getText() != null && txtSkill.getText().trim() != null){
            skill = new Skill(0, txtSkill.getText());
            skillDao.insertSkill(skill);
        }
        else{
            skill = (Skill) cbSkill.getSelectedItem();
        }
        
        int power = sliderPower.getValue();
        
        userSkillDao.insertUserSkill(new UserSkill(0, loggedInUser, skill, power));
        cbSkill.removeAllItems();
        fillWindows();
        
        
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int index = tblSkill.getSelectedRow();
        UserSkill userSkill = userSkills.get(index);
        userSkillDao.removeUserSkill(userSkill.getId());
        cbSkill.removeAllItems();
        fillWindows();
    }//GEN-LAST:event_btnRemoveActionPerformed


