    private void fillWindows(){
        skills = skillDao.getAll();
        
        for(Skill skill : skills){
            cbSkill.addItem(skill);
        }
        fillTable();
    }
    
    private List<UserSkill> userSkills;
    private void fillTable(){
        userSkills = userSkillDao.getAllSkillByUserId(Config.loggedInUser.getId());

        Vector rows = new Vector();
        
        for(UserSkill item : userSkills){
            Vector row = new Vector();
            row.add(item.getSkill());
            row.add(item.getPower());
            rows.add(row);
        }
        DefaultTableModel model = new DefaultTableModel(rows, new Vector(Arrays.asList(new String[]{"Skills", "Power"})));     
        tblSkill.setModel(model);
    }
    
